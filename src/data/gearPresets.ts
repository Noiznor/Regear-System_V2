export const getPresetsByRole = (role: string) => {
  switch (role) {
    case 'tank':
      return tankPresets;
    case 'dps':
      return dpsPresets;
    case 'support':
      return supportPresets;
    case 'healer':
      return healerPresets;
    default:
      return {};
  }
};